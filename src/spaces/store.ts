function spacesRoot(): string {
  return process.env.MEMU_SPACES_ROOT ?? path.resolve(process.cwd(), 'data', 'spaces');
}

function familyDir(familyId: string): string {
  return path.join(spacesRoot(), familyId);
}

function filePath(familyId: string, category: SpaceCategory, slug: string): string {
  return path.join(familyDir(familyId), `${category}s`, `${slug}.md`);
}

function logPath(familyId: string): string {
  return path.join(familyDir(familyId), '_log.md');
}

async function ensureFamilyRepo(familyId: string): Promise<void> {
  const dir = familyDir(familyId);
  await fs.mkdir(dir, { recursive: true });
  const gitDir = path.join(dir, '.git');
  let freshlyInitialised = false;
  try {
    await fs.access(gitDir);
  } catch {
    // Git is the version-history substrate for the Spaces directory but
    // the DB row + on-disk markdown are the source of truth. If git is
    // unavailable (binary missing, ENOENT, sandbox restrictions),
    // version history is degraded but Spaces still work — don't crash
    // the Space write. Same pattern as the inner add/commit below.
    try {
      execFileSync('git', ['init', '-q', '-b', 'main'], { cwd: dir });
      execFileSync('git', ['config', 'user.email', 'memu@localhost'], { cwd: dir });
      execFileSync('git', ['config', 'user.name', 'Memu'], { cwd: dir });
      freshlyInitialised = true;
    } catch (err) {
      console.warn('[SPACES] git init non-fatal — Space history disabled:', (err as Error).message ?? err);
    }
  }
  await ensureReadme(familyId, freshlyInitialised);
}

async function resolveActor(actorProfileId: string | undefined): Promise<{ name: string; email: string }> {
  if (!actorProfileId) return { name: 'Memu', email: 'memu@localhost' };
  try {
    const res = await db.query<{ display_name: string; email: string | null }>(
      `SELECT display_name, email FROM profiles WHERE id = $1 LIMIT 1`,
      [actorProfileId],
    );
    const row = res.rows[0];
    if (!row) return { name: 'Memu', email: 'memu@localhost' };
    return {
      name: row.display_name || 'Memu',
      email: row.email ?? `${actorProfileId}@memu.local`,
    };
  } catch {
    return { name: 'Memu', email: 'memu@localhost' };
  }
}

