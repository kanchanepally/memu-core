/**
 * Story 2.1 — Spaces store.
 *
 * The directory `spaces/<family_id>/<category>/<slug>.md` is the
 * source of truth for compiled understanding, alongside the database.
 * Writes go to both: DB row first (atomic, transactional), then disk +
 * git commit. The DB row is the programmatic read path; the directory
 * is the human-auditable, git-versioned, Article-20-exportable form.
 *
 * Every write is committed to a local git repo rooted at
 * MEMU_SPACES_ROOT (default: ./data/spaces). This gives us free
 * version history and blame for synthesis updates without any
 * external services.
 */

